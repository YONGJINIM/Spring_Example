package com.example.lesson01;


// 일반 java bean으로 사용하겠다. 
public class Data {
	
	// field 
	private int id; 
	private String name;
	
	// method
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
	
}
